gen.inv1 <-
function(mat, thresh = .05)
{
        v1 <- sum(is.na(mat))
        v2 <- sum(is.inf(mat))
        if((v1 + v2) > 0.5) {
                print(mat)
        }
        e1 <- eigen(mat, symmetric = T)
        val <- Re(e1$val)
        vec <- Re(e1$vec)
        val1 <- val/max(val)
        #
        #       print("normalized eigen values")
        #       print(val1)     #
        #       n1 <- length(val1)
        #       plot(c(1:n1), abs(val1), log = "y", xlab = "eigen rank", ylab
        #                = "log10 of value")
        I1 <- val1 > thresh
        I3 <- is.na(I1)
        if(sum(I3) < 0.5) {
                val2 <- val[I1]
                I2 <- I1
                if(sum(I2) > 1.5) {
                        ret <- vec[, I1] %*% diag(1/val2) %*% t(vec[, I1])
                }
                else {
                        v1 <- as.matrix(vec[, I1], length(c(vec[, I1])), 1)
                        ret <- (1/val2) * v1 %*% t(v1)
                }
        }
        else {
                ret <- diag(length(I1)) * 0
        }
        ret
}
