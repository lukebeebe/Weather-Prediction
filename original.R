seas.adj.pred <-
function(v1a, per = 4)
{
	n1a<-length(v1a)
	n1<-n1a-20
	v1<-v1a[1:n1]
	n1 <- length(v1)
	n2 <- floor(n1/per - 1)
	n2a<-floor(n1a/per-1)
	if((n2*per+per)<(n1-.1)){
		n3vec<-c((n2*per+per):n1)
	}
	if((n2a*per+per)<(n1a-.1)){
		n3veca<-c((n2a*per+per):n1a)
	}

	for(j in 1:per) {
		I1 <- c(0:n2) * per + j
		v2 <- v1[I1]
		I2<-c(0:n2a)*per+j
		v2a<-v1a[I2]
		m2 <- mean(v2)
		v2 <- v2 - mean(v2)
		v2a<-v2a-m2
		v1[I1] <- v2
		v1a[I2]<-v2a
		if((n2*per+per)<(n1-.1)){
		ind.plus<-((max(I1+per))==n3vec)
		v1[n3vec[ind.plus]]<-v1[n3vec[ind.plus]]-m2
		}
		if((n2a*per+per)<(n1a-.1)){
		ind.plusa<-((max(I2+per))==n3veca)
		v1a[n3veca[ind.plusa]]<-v1a[n3veca[ind.plusa]]-m2
		}

	}
	v1a
}
